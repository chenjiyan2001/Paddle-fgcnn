from __future__ import print_function
import pandas as pd

from paddle.io import IterableDataset


class RecDataset(IterableDataset):
    def __init__(self, file_list, config):
        super(RecDataset, self).__init__()
        self.file_list = file_list
        self.feature_name = ['hour', 'C1', 'banner_pos', 'site_id', 'site_domain', 'site_category', 'app_id', 'app_domain', 'app_category', 'device_id', 'device_model', 'device_type', 'device_conn_type', 'C14', 'C15', 'C16', 'C17', 'C18', 'C19', 'C20', 'C21']

    def __iter__(self):
        for file in self.file_list:
            rf = pd.HDFStore(file)
            data = rf['data'][self.feature_name]
            label = rf['label']
            for d, l in zip(data.iterrows(), label):
                yield d[1].values.tolist() + [l]